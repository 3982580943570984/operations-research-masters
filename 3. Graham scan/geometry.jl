"""
    cross(a::Point, b::Point, c::Point)

Вычисляет z-компоненту векторного произведения AB x AC

Знак результата:
- > 0: левый поворот
- < 0: правый поворот
- = 0: коллинеарные точки
"""
cross(a::Point, b::Point, c::Point) = (b.x - a.x) * (c.y - a.y) - (b.y - a.y) * (c.x - a.x)

"""
    distance(a::Point, b::Point)

Возвращает квадрат евклидова расстояния между точками a и b

Используется для сравнения расстояний без вычисления sqrt
"""
distance(a::Point, b::Point) = (a.x - b.x)^2 + (a.y - b.y)^2
